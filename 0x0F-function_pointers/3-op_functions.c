#include "3-calc.h"

/**
 * op_add - function that returns the sum of a and b
 * @a: first input integer number
 * @b: second input integer number
 *
 * Return: the addition of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -function that returns the subtration of a and b
 * @a: first input integer number
 * @b: second input integer number
 *
 * Return: the subtration of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -function that returns the multiplication of a and b
 * @a: first input integer number
 * @b: second input integer number
 *
 * Return: the multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -function that returns the division of a and b
 * @a: first input integer number
 * @b: second input integer number
 *
 * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -function that returns the remainder of the division of a and b
 * @a: first input integer number
 * @b: second input integer number
 *
 * Return: the remainder of the division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
